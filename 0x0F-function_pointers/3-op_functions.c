#include "3-calc.h"
/**
 * op_add - adds
 * @a: first int
 * @b: second int
 * Return: sum
*/
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - sub
 * @a: first int
 * @b: second int
 * Return: difference
*/
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - multiply
 * @a: first int
 * @b: second int
 * Return: product
*/
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - devid
 * @a: first int
 * @b: second int
 * Return: division
*/
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - mod
 * @a: first int
 * @b: second int
 * Return:  remainder of the division
*/
int op_mod(int a, int b)
{
return (a % b);
}
